#Example of FOR
print('My name is')
for i in range(5):
    print('Victor Five Times ' + str(i))

#Equal of this While
print('My name is')
i = 0
while i < 5:
    print('Victor Five Times ' + str(i))
    i = i + 1

#Another FOR example
total = 0
for num in range(101):
    total = total + num
print(total)

#Another example of FOR
print('My name is')
for i in range(12,16):  #begin on 12, goes until 15
    print('Victor Five Times ' + str(i))

#Another example of FOR
print('My name is')
for i in range(0,10, 2):  #begin on 0, until 10, jumping 2 in 2
    print('Victor Five Times ' + str(i))

